public class Relationaloperator {

	public static void main(String[] args) {
		int a=7, b=11;
		//value of a and b
		System.out.println("Value of a is" + a +"and b is"+ b);
		//==operator
		System.out.println(a==b);//false
		//!=operator
		System.out.println(a!=b);//true
		//a<b
		System.out.println(a<b);//true
		//a>b
		System.out.println(a>b);//false
		//a<=b
		System.out.println(a<=b);//true
		//a>=b
		System.out.println(a>=b);//false

	}

}
